"""
Description: Tutorial on how to use "keyword argument"
"""

def dumb_sentence(nam='Bucky', action='ate', item='tuna'):
	print(name, action, item)

dumb_sentence()
dumd_sentence('Saly', 'farts', 'gently')
dumd_sentence(item='awesome')
dumb_sentence(item='awesome', action='is')
